-- 創建 Categories 表 (分類表)
CREATE TABLE ArticleCategories (
    id INT AUTO_INCREMENT PRIMARY KEY,  -- 自動增長的分類 ID
    name VARCHAR(255) NOT NULL,         -- 分類名稱
    description TEXT,                   -- 分類描述
    parent_category_id INT,             -- 父分類的 ID，為 NULL 時表示是大分類
    FOREIGN KEY (parent_category_id) REFERENCES ArticleCategories(id)  -- 外鍵約束，指向父分類
);

-- 創建 Articles 表 (文章表)
CREATE TABLE Articles (
    id INT AUTO_INCREMENT PRIMARY KEY,  -- 文章 ID
    title VARCHAR(255) NOT NULL,        -- 文章標題
    introduction TEXT,                  -- 文章介紹
    content TEXT NOT NULL,              -- 文章內容
    category_id INT,                    -- 關聯分類的 ID
    views INT DEFAULT 0,                -- 文章觀看次數
    imageURL VARCHAR(255),                 -- 文章圖片 URL 或檔案名
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,  -- 創建時間
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (category_id) REFERENCES ArticleCategories(id)  -- 外鍵約束，指向 Categories 表
);

-- 創建 UserBookmarkedArticles 表 (用戶收藏文章)
CREATE TABLE UserBookmarkedArticles (
    id INT AUTO_INCREMENT PRIMARY KEY,   -- 自動增長的收藏 ID
    user_id INT NOT NULL,                -- 用戶 ID，指向 Users 表的 ID
    article_id INT NOT NULL,             -- 文章 ID，指向 Articles 表的 ID
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,  -- 收藏時間
    FOREIGN KEY (user_id) REFERENCES Users(id),  -- 外鍵約束，指向 Users 表的 ID
    FOREIGN KEY (article_id) REFERENCES Articles(id)  -- 外鍵約束，指向 Articles 表的 ID
);

